#include <unistd.h>

void	ft_putnbr(int n)
{
	int		c;

	c = 0;
	if (n == -2147483648)
		write(1, "-2147483648", 11);
	else
	{
		if (n < 0)
		{
			n = -n;
			write(1, "-", 1);
		}
		if (n / 10 > 0)
			ft_putnbr(n / 10);
		c = (n % 10) + 48;
		write(1, &c, 1);
	}
}
