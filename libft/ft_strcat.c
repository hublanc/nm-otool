#include <string.h>
#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	size_t	i;
	size_t	dest_len;

	if (!s2)
		return (s1);
	dest_len = ft_strlen(s1);
	i = 0;
	while (s2[i])
	{
		s1[dest_len + i] = s2[i];
		i++;
	}
	s1[dest_len + i] = '\0';
	return (s1);
}
